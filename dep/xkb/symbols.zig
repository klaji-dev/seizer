const std = @import("std");

pub const Symbol = make_enum: {
    var enum_fields: [SYMBOLS.len]std.builtin.Type.EnumField = undefined;
    for (&enum_fields, SYMBOLS) |*enum_field, symbol_description| {
        enum_field.* = .{
            .name = symbol_description.name,
            .value = symbol_description.keycode,
        };
    }

    break :make_enum @Type(std.builtin.Type.Enum{
        .tag_type = u29,
        .fields = &enum_fields,
        .decls = &.{},
        .is_exhaustive = false,
    });
};

/// Caller owns the HashMap
pub fn initUnicodeHashMap(allocator: std.mem.Allocator) !std.AutoHashMap(Symbol, u21) {
    var hash_map = std.AutoHashMap(Symbol, u21).init(allocator);
    errdefer hash_map.deinit();
    for (SYMBOLS) |symbol_description| {
        const character = symbol_description.unicode_character orelse continue;
        const symbol: Symbol = @enumFromInt(symbol_description.keycode);
        try hash_map.put(symbol, character);
    }
    return hash_map;
}

